

订单表字段：

订单号/用户ID/手机号码/提交时间/更新时间
订单类型/物流单号/商品型号/备注说明/
吊牌价格/支付系数/支付价格/
订单状态/操作/操作者


【说明】
订单号：日期+自增4位数1001开始，例如1801011001
用户ID：随机数字，8位自增
手机号码：13位数字
订单类型：for_card(兑换现金券)/for_new（旧品换新）/for_mend（商品维修）
物流单号：来自于用户填写，字母+数字
商品型号：字母+数字
商品图片1：来自于用户上传
商品图片2：来自于用户上传
备注说明：来自于用户填写，默认空，有地址信息等
吊牌价格:市场价格，例：100.00
支付系数：折扣率，例：0.90
支付价格:实际支付价格，例如：90.00
订单状态：用户已提交/商家已确认/用户待支付/用户已支付
操作：用户提交/商家确认/用户支付/
操作者：13位数字，手机号啊


